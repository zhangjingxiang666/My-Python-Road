from turtle import *
def birthday():
    setup(1600,800,0,0)
    pensize(20)
    pencolor("red")
    penup()
    goto(129,100)
    pendown()
    left(90)
    circle(100,180)
    right(45)
    penup()
    goto(129,100)
    pendown()
    fd(250)
    left(135)
    fd(200)
    penup()
    goto(229, 200)
    pendown()
    fd(200)
    penup()
    goto(229, 200)
    pendown()
    right(90)
    fd(130)
    left(90)
    circle(-120, 180)
birthday()